function toggleReadBtn(t) {
    if (t.innerHTML == "Read More"){

        t.innerHTML = "Read Less";
    } 
    else {
        t.innerHTML = "Read More";
    }
}

function toggleThemeBtn(t) {
    if (t.innerHTML == "See More"){

        t.innerHTML = "See Less";
    } 
    else {
        t.innerHTML = "See More";
    }
}